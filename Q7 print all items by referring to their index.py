'''  a python program to Print all items by 
referring to their index number (thislist =
["Java", "SQL", "C", "Reactnative", "Javascript", 
"Python"] '''

list=["Java", "SQL", "C", "Reactnative", "Javascript", 
"Python"]
length=len(list)
i=0
while i<length:
    print(list[i],end=' >')
    i+=1

    
    
     